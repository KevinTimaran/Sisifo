
class Datos:
    def __init__(self, dato1, signo, dato2):
        self.dato1 = dato1
        self.signo = signo
        self.dato2 = dato2

    def darDato1 (self):
        return self.dato1
    
    def darOperacion (self):
        return self.signo
    
    def darDato2 (self):
        return self.dato2
        
    
